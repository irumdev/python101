# 1. 비교 연산
print("1. 비교연산")
print(2 > 3)  # False
print(15 < 30) # True
print(1.5 >= 0)  # True
print(3 <= 3)  # True
print("안녕하세요?" == "안녕하세요!")   # False
print("1234" != "1234")  # False


# 2. 논리 연산
print("2. 논리연산")
print(4 < 6 and 10 >= 10) # True
print("포기하지말아요" != "포기하지말아요" or "나는 할 수 있다" == "나는 할 수 있다")  # True
print(not 5 == 5)  # False


# 3. 멤버십 연산
print("3. 멤버십 연산")
print("a" in "abc")  # True
print("d" not in "abc")  # True
