# 변수 값 초기화
name = "콜라"
calorie = 300
volume = 320
unit = "ml"
print(name, calorie, volume, unit)


# 변수 값 변경
name = "사이다"
calorie = 680
volume = 1
unit = "l"
print(name, calorie, volume, unit)


# Indirection
name = "칠성" + name
calorie = calorie + 100
volume = volume + 1
unit = "l"
print(name, calorie, volume, unit)
